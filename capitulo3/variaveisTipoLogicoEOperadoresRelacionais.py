""" Comparações lógicas são feitas por meio de operadores relacionais 
(linguagem python) representa (símbolo matemático)
== representa = 
> representa >
< representa <
!= representa diferente
>= representa maior ou igual que
<= representa menor ou igual que 
"""

a = 1 # a igual a 1
b = 5 # b igual a 5
c = 2 # c igual a 2
d = 1 # d igual a 1

print("a é igual a b?", a == b) #falso
print("b é maior que a?", b > a) #verdadeiro
print("a é menor que b?", a < b) #verdadeiro
print("a é igual a d?", a == d) #verdadeiro
print("b é maior ou igual a a?", b >= a)#verdadeiro
print("c é menor ou igual a b?", c <= b) #verdadeiro
print("d é diferente de a?", d != a) #falso
print("d é diferente de b?", d != b) #verdadeiro
